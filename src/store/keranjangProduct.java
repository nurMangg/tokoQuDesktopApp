    private void tblKeranjangKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblKeranjangKeyReleased

        int baris = tblKeranjang.getSelectedRow();

        String harga = tblKeranjang.getValueAt(baris, 2).toString();
        String banyaknya = tblKeranjang.getValueAt(baris, 3).toString();

        int total = Integer.parseInt(harga) * Integer.parseInt(banyaknya);

        tblKeranjang.setValueAt(total, baris, 4);

        int totalHarga = 0;
        for (int i = 0; i < tblKeranjang.getRowCount(); i++) {
            String totalbrg = tblKeranjang.getValueAt(i, 4).toString();
            int totalnya = Integer.parseInt(totalbrg);
            totalHarga += totalnya;
        }

        txttotalHarga.setText(String.valueOf(totalHarga));
    }//GEN-LAST:event_tblKeranjangKeyReleased

