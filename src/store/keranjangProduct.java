    public String getIdTransaksi() {
        String idtrx = "";
        try {
            Connection c = Koneksi.konekKeDB();
            Statement sti = c.createStatement();
            String query = "SELECT kode_tr FROM transactionQ ORDER BY kode_tr DESC LIMIT 1;";
            ResultSet rs = sti.executeQuery(query);
            while (rs.next()) {
                idtrx = rs.getString("kode_tr");
            }

            dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

        return idtrx;
    }

    public void setAdmin() {
        try {
            Connection c = Koneksi.konekKeDB();
            Statement sti = c.createStatement();
            String query = "SELECT * FROM users;";
            ResultSet rs = sti.executeQuery(query);
            while (rs.next()) {
                String nama = rs.getString("nama");

                cmbUser.addItem(nama);
            }

            dispose();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void simpanTransaksi() {
        String total = txttotalHarga.getText();
        String uang = txtuang.getText();
        String kembali = txtKembali.getText();
        int user = cmbUser.getSelectedIndex() + 1;
        String date = txtDate.getText();

        try {
            Connection c = Koneksi.konekKeDB();
            Statement sti = c.createStatement();
            String query = "INSERT INTO transactionQ VALUES(NULL,NULL," + user + "," + total + "," + uang + "," + kembali + ",'" + date + "');";
            sti.executeUpdate(query);

            for (int i = 0; i < tblKeranjang.getRowCount(); i++) {
                String kodebarang = (String) tblKeranjang.getValueAt(i, 0);
                String qtybarang = (String) tblKeranjang.getValueAt(i, 3);
                String idQ = getIdTransaksi();

                Statement st2 = c.createStatement();
                String query2 = "INSERT INTO detail_transaction VALUES(NULL,'" + idQ + "','" + kodebarang + "'," + qtybarang + ");";
                st2.executeUpdate(query2);
            }

            c.close();
            JOptionPane.showMessageDialog(null, "Transaksi Berhasil di Simpan");

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }
